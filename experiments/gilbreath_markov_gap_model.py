"""Test Gilbreath behavior against first-order Markov prime-gap controls."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import certified_lock_scan, markov_gap_sequence  # noqa: E402
from emanation_from_1.number_theory import first_n_primes  # noqa: E402


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def parse_csv_ints(value: str, label: str) -> list[int]:
    values = [int(part.strip()) for part in value.split(",") if part.strip()]
    if not values or any(item < 1 for item in values):
        raise ValueError(f"{label} must be a comma-separated list of positive integers")
    return values


def markov_rows(args: argparse.Namespace) -> list[list[object]]:
    rows: list[list[object]] = []

    for prime_count in parse_csv_ints(args.prime_counts, "prime-counts"):
        primes = first_n_primes(prime_count)
        prime_scan = certified_lock_scan(primes)

        certified_count = 0
        lock_rows: list[int] = []
        failure_rows: list[int] = []

        for offset in range(args.trials):
            seed = args.seed + (prime_count * args.trials) + offset
            control = markov_gap_sequence(primes, seed)
            scan = certified_lock_scan(control)
            if scan.certified:
                certified_count += 1
                lock_rows.append(scan.certified_lock_row or 0)
            elif scan.first_failure is not None:
                failure_rows.append(scan.first_failure[0])

        average_lock = "none" if not lock_rows else f"{sum(lock_rows) / len(lock_rows):.2f}"
        average_failure = (
            "none" if not failure_rows else f"{sum(failure_rows) / len(failure_rows):.2f}"
        )

        rows.append(
            [
                prime_count,
                "none" if prime_scan.certified_lock_row is None else prime_scan.certified_lock_row,
                args.trials,
                certified_count,
                f"{certified_count / args.trials:.3f}",
                average_lock,
                average_failure,
            ]
        )

    return rows


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prime-counts", default="256,512,1024,2048,4096")
    parser.add_argument("--trials", type=int, default=100)
    parser.add_argument("--seed", type=int, default=40000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_MARKOV_GAP_MODEL.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    if args.trials < 1:
        raise ValueError("trials must be >= 1")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    rows = markov_rows(args)

    report = "\n\n".join(
        [
            "# Gilbreath Markov Gap Model",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_markov_gap_model.py`.",
            "Each control sequence is generated from the empirical one-step transition map of prime gaps in the same prefix.",
            "This preserves the first gap and samples each next gap from gaps that followed the current gap in the real prime prefix.",
            "The model preserves local gap-transition statistics but not long-range prime-gap order.",
            "These are finite controls, not a proof of the infinite Gilbreath conjecture.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["prime_counts", args.prime_counts],
                    ["trials_per_prefix", args.trials],
                    ["seed_start", args.seed],
                ],
            ),
            "## Markov Control Results",
            markdown_table(
                [
                    "prime_count",
                    "prime_lock_row",
                    "trials",
                    "certified_locks",
                    "lock_rate",
                    "avg_lock_row",
                    "avg_failure_row",
                ],
                rows,
            ),
            "## Local Interpretation",
            "If Markov controls remain low-rate while prime prefixes lock, then one-step gap transitions are not enough to explain the boundary-return behavior.",
            "If Markov controls approach the prime lock rate, the effect may be mostly local transition structure.",
            "A stronger future control would preserve longer transition history or residue-class wheel constraints.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()

