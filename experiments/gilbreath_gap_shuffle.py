"""Test Gilbreath behavior against shuffled prime-gap controls."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import (  # noqa: E402
    boundary_return_check,
    difference_rows,
    first_certificate_row,
    shuffled_tail_gap_sequence,
)
from emanation_from_1.number_theory import first_n_primes  # noqa: E402


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def certified_lock(sequence: list[int]) -> tuple[bool, int | None, tuple[int, int] | None]:
    rows = difference_rows(sequence)
    boundary = boundary_return_check(sequence)
    certificate = first_certificate_row(rows)
    return boundary.verified and certificate is not None, certificate, boundary.first_failure


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prime-count", type=int, default=256)
    parser.add_argument("--trials", type=int, default=200)
    parser.add_argument("--seed", type=int, default=9000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_GAP_SHUFFLE.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    if args.prime_count < 2:
        raise ValueError("prime-count must be >= 2")
    if args.trials < 1:
        raise ValueError("trials must be >= 1")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    primes = first_n_primes(args.prime_count)
    prime_lock, prime_certificate, prime_failure = certified_lock(primes)

    certified_count = 0
    certificate_count = 0
    boundary_pass_count = 0
    failure_rows: list[int] = []
    certificate_rows: list[int] = []
    sample_rows: list[list[object]] = []

    for offset in range(args.trials):
        seed = args.seed + offset
        shuffled = shuffled_tail_gap_sequence(primes, seed)
        lock, certificate, failure = certified_lock(shuffled)
        if lock:
            certified_count += 1
        if certificate is not None:
            certificate_count += 1
            certificate_rows.append(certificate)
        if failure is None:
            boundary_pass_count += 1
        else:
            failure_rows.append(failure[0])
        if offset < 16:
            sample_rows.append(
                [
                    seed,
                    "yes" if lock else "no",
                    "none" if certificate is None else certificate,
                    "none" if failure is None else failure,
                ]
            )

    average_certificate = (
        "none"
        if not certificate_rows
        else f"{sum(certificate_rows) / len(certificate_rows):.2f}"
    )
    average_failure = (
        "none" if not failure_rows else f"{sum(failure_rows) / len(failure_rows):.2f}"
    )

    report = "\n\n".join(
        [
            "# Gilbreath Prime-Gap Shuffle",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_gap_shuffle.py`.",
            "Each control sequence keeps the exact prime-gap multiset for the chosen prefix, fixes the first gap `1`, and shuffles the remaining gaps.",
            "This tests whether the observed finite Gilbreath behavior depends on prime-gap order, not merely gap sizes.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["prime_count", args.prime_count],
                    ["shuffle_trials", args.trials],
                    ["seed_start", args.seed],
                ],
            ),
            "## Prime Prefix",
            markdown_table(
                ["boundary", "certificate_row", "first_failure"],
                [
                    [
                        "pass" if prime_lock else "fail",
                        "none" if prime_certificate is None else prime_certificate,
                        "none" if prime_failure is None else prime_failure,
                    ]
                ],
            ),
            "## Shuffled Gap Controls",
            markdown_table(
                [
                    "trials",
                    "certified_locks",
                    "certified_lock_rate",
                    "boundary_passes",
                    "certificates_found",
                    "avg_certificate_row",
                    "avg_failure_row",
                ],
                [
                    [
                        args.trials,
                        certified_count,
                        f"{certified_count / args.trials:.3f}",
                        boundary_pass_count,
                        certificate_count,
                        average_certificate,
                        average_failure,
                    ]
                ],
            ),
            "## First Shuffle Samples",
            markdown_table(
                ["seed", "certified_lock", "certificate_row", "first_failure"],
                sample_rows,
            ),
            "## Local Interpretation",
            "If shuffled prime-gap controls usually fail before lock while the prime prefix locks, the gap order is carrying information not captured by the gap multiset alone.",
            "That would be a candidate support signal for the Origin Reframe, but still not proof; the next control would compare against known prime-gap dependency models.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()

