"""Prefix scaling and block-shuffled prime-gap controls for Gilbreath behavior."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import (  # noqa: E402
    block_shuffled_tail_gap_sequence,
    certified_lock_scan,
)
from emanation_from_1.number_theory import first_n_primes  # noqa: E402


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def parse_csv_ints(value: str, label: str) -> list[int]:
    values = [int(part.strip()) for part in value.split(",") if part.strip()]
    if not values or any(item < 1 for item in values):
        raise ValueError(f"{label} must be a comma-separated list of positive integers")
    return values


def prefix_rows(args: argparse.Namespace) -> list[list[object]]:
    rows: list[list[object]] = []
    for prime_count in parse_csv_ints(args.prime_counts, "prime-counts"):
        primes = first_n_primes(prime_count)
        scan = certified_lock_scan(primes)
        rows.append(
            [
                prime_count,
                "yes" if scan.certified else "no",
                "none" if scan.certified_lock_row is None else scan.certified_lock_row,
                "none" if scan.first_failure is None else scan.first_failure,
                scan.rows_scanned,
            ]
        )
    return rows


def block_shuffle_rows(args: argparse.Namespace) -> list[list[object]]:
    rows: list[list[object]] = []
    prime_counts = parse_csv_ints(args.prime_counts, "prime-counts")
    block_sizes = parse_csv_ints(args.block_sizes, "block-sizes")

    for prime_count in prime_counts:
        primes = first_n_primes(prime_count)
        prime_scan = certified_lock_scan(primes)

        for block_size in block_sizes:
            certified_count = 0
            failure_rows: list[int] = []
            lock_rows: list[int] = []

            for offset in range(args.trials):
                seed = args.seed + (prime_count * 100_000) + (block_size * args.trials) + offset
                shuffled = block_shuffled_tail_gap_sequence(primes, block_size, seed)
                scan = certified_lock_scan(shuffled)
                if scan.certified:
                    certified_count += 1
                    lock_rows.append(scan.certified_lock_row or 0)
                elif scan.first_failure is not None:
                    failure_rows.append(scan.first_failure[0])

            average_lock = "none" if not lock_rows else f"{sum(lock_rows) / len(lock_rows):.2f}"
            average_failure = (
                "none" if not failure_rows else f"{sum(failure_rows) / len(failure_rows):.2f}"
            )
            rows.append(
                [
                    prime_count,
                    "none" if prime_scan.certified_lock_row is None else prime_scan.certified_lock_row,
                    block_size,
                    args.trials,
                    certified_count,
                    f"{certified_count / args.trials:.3f}",
                    average_lock,
                    average_failure,
                ]
            )

    return rows


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prime-counts", default="64,128,256,512,1024")
    parser.add_argument("--block-sizes", default="1,2,4,8,16,32")
    parser.add_argument("--trials", type=int, default=40)
    parser.add_argument("--seed", type=int, default=15000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_PREFIX_BLOCK_SHUFFLE.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    if args.trials < 1:
        raise ValueError("trials must be >= 1")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    prefix_summary = prefix_rows(args)
    control_summary = block_shuffle_rows(args)

    report = "\n\n".join(
        [
            "# Gilbreath Prefix Scaling And Block Shuffle",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_prefix_block_shuffle.py`.",
            "The prime-prefix scan measures whether each prefix reaches a certified lock row before any first-column failure.",
            "The block-shuffle controls preserve the first prime gap and preserve local gap order inside each shuffled block.",
            "Block size `1` is full tail-gap shuffle. Larger block sizes preserve more local prime-gap dependency.",
            "These are finite controls, not a proof of the infinite Gilbreath conjecture.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["prime_counts", args.prime_counts],
                    ["block_sizes", args.block_sizes],
                    ["trials_per_cell", args.trials],
                    ["seed_start", args.seed],
                ],
            ),
            "## Prime Prefix Scaling",
            markdown_table(
                [
                    "prime_count",
                    "certified_lock",
                    "lock_row",
                    "first_failure",
                    "rows_scanned",
                ],
                prefix_summary,
            ),
            "## Block-Shuffled Prime-Gap Controls",
            markdown_table(
                [
                    "prime_count",
                    "prime_lock_row",
                    "block_size",
                    "trials",
                    "certified_locks",
                    "lock_rate",
                    "avg_lock_row",
                    "avg_failure_row",
                ],
                control_summary,
            ),
            "## Local Interpretation",
            "The strongest evidence would be a stable prime-prefix lock across growing prefixes while block-shuffled controls remain low-rate, even for larger block sizes.",
            "If block-shuffled controls approach the prime lock rate as block size grows, the effect may be explained by local prime-gap dependency rather than a more global Origin-frame structure.",
            "This experiment helps locate the scale at which prime-gap order starts carrying the boundary-return behavior.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()

