"""Run local control tests for Gilbreath-style boundary behavior."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import (  # noqa: E402
    boundary_return_check,
    consecutive_odd_sequence,
    odd_arithmetic_after_boundary,
    random_odd_small_gap_sequence,
)
from emanation_from_1.number_theory import first_n_primes  # noqa: E402


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def check_rows(args: argparse.Namespace) -> list[list[object]]:
    checks = [
        boundary_return_check(first_n_primes(args.length), "prime prefix"),
        boundary_return_check(
            consecutive_odd_sequence(args.length),
            "consecutive odd control",
        ),
        boundary_return_check(
            odd_arithmetic_after_boundary(args.length, gap=6),
            "gap-6 odd control",
        ),
    ]

    rows: list[list[object]] = []
    for check in checks:
        rows.append(
            [
                check.name,
                check.length,
                check.rows_checked,
                "pass" if check.verified else "fail",
                "none" if check.first_failure is None else check.first_failure,
                list(check.first_column_prefix),
            ]
        )
    return rows


def random_rows(args: argparse.Namespace) -> tuple[list[list[object]], int]:
    rows: list[list[object]] = []
    pass_count = 0

    for offset in range(args.random_trials):
        seed = args.seed + offset
        sequence = random_odd_small_gap_sequence(
            args.length,
            max_gap_units=args.random_max_gap_units,
            seed=seed,
        )
        check = boundary_return_check(sequence, f"random seed {seed}")
        if check.verified:
            pass_count += 1
        rows.append(
            [
                seed,
                "pass" if check.verified else "fail",
                "none" if check.first_failure is None else check.first_failure,
                list(check.first_column_prefix),
            ]
        )

    return rows, pass_count


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--length", type=int, default=128)
    parser.add_argument("--random-trials", type=int, default=12)
    parser.add_argument("--random-max-gap-units", type=int, default=4)
    parser.add_argument("--seed", type=int, default=1000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_CONTROL_EXPERIMENTS.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    deterministic_rows = check_rows(args)
    seeded_rows, pass_count = random_rows(args)

    report = "\n\n".join(
        [
            "# Gilbreath Control Experiments",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_controls.py`.",
            "A pass means every finite generated difference row begins with `1` for this specific input sequence.",
            "A fail means at least one generated row has a first value other than `1`.",
            "These are finite local tests, not proofs of infinite behavior.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["length", args.length],
                    ["random_trials", args.random_trials],
                    ["random_max_gap_units", args.random_max_gap_units],
                    ["seed_start", args.seed],
                ],
            ),
            "## Deterministic Controls",
            markdown_table(
                [
                    "sequence",
                    "length",
                    "rows checked",
                    "result",
                    "first failure",
                    "first-column prefix",
                ],
                deterministic_rows,
            ),
            "## Seeded Random Small-Gap Controls",
            f"Passed `{pass_count}` of `{args.random_trials}` seeded trials.",
            markdown_table(
                ["seed", "result", "first failure", "first-column prefix"],
                seeded_rows,
            ),
            "## Local Interpretation",
            "The prime prefix pass is reproducible for the chosen finite length.",
            "The consecutive odd control also passes, so first-column `1` behavior is not prime-specific in this finite test.",
            "The gap-6 odd control fails, so the behavior is not automatic for every odd sequence beginning with `2, 3`.",
            "The seeded random controls are measurements only; they do not establish an asymptotic theorem.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
