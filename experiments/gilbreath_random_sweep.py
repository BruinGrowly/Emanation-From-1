"""Sweep random small-gap sequences for Gilbreath certified-lock rates."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import (  # noqa: E402
    boundary_return_check,
    difference_rows,
    first_certificate_row,
    random_odd_small_gap_sequence,
)


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def parse_gap_units(value: str) -> list[int]:
    gap_units = [int(part.strip()) for part in value.split(",") if part.strip()]
    if not gap_units or any(unit < 1 for unit in gap_units):
        raise ValueError("gap units must be a comma-separated list of positive integers")
    return gap_units


def trial_result(length: int, max_gap_units: int, seed: int) -> tuple[bool, int | None, tuple[int, int] | None]:
    sequence = random_odd_small_gap_sequence(length, max_gap_units, seed)
    rows = difference_rows(sequence)
    boundary = boundary_return_check(sequence)
    certificate = first_certificate_row(rows)
    certified_lock = boundary.verified and certificate is not None
    return certified_lock, certificate, boundary.first_failure


def sweep_rows(args: argparse.Namespace) -> list[list[object]]:
    rows: list[list[object]] = []
    for max_gap_units in parse_gap_units(args.max_gap_units):
        certified_count = 0
        certificate_count = 0
        boundary_pass_count = 0
        first_failure_rows: list[int] = []
        certificate_rows: list[int] = []

        for offset in range(args.trials):
            seed = args.seed + (max_gap_units * args.trials) + offset
            certified, certificate, failure = trial_result(
                args.length,
                max_gap_units,
                seed,
            )
            if certified:
                certified_count += 1
            if certificate is not None:
                certificate_count += 1
                certificate_rows.append(certificate)
            if failure is None:
                boundary_pass_count += 1
            else:
                first_failure_rows.append(failure[0])

        average_certificate = (
            "none"
            if not certificate_rows
            else f"{sum(certificate_rows) / len(certificate_rows):.2f}"
        )
        average_failure = (
            "none"
            if not first_failure_rows
            else f"{sum(first_failure_rows) / len(first_failure_rows):.2f}"
        )

        rows.append(
            [
                max_gap_units,
                args.trials,
                certified_count,
                f"{certified_count / args.trials:.3f}",
                boundary_pass_count,
                certificate_count,
                average_certificate,
                average_failure,
            ]
        )

    return rows


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--length", type=int, default=256)
    parser.add_argument("--trials", type=int, default=100)
    parser.add_argument("--max-gap-units", default="1,2,3,4,5,6,8")
    parser.add_argument("--seed", type=int, default=5000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_RANDOM_SWEEP.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    if args.length < 2:
        raise ValueError("length must be >= 2")
    if args.trials < 1:
        raise ValueError("trials must be >= 1")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    rows = sweep_rows(args)

    report = "\n\n".join(
        [
            "# Gilbreath Random Sweep",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_random_sweep.py`.",
            "Each trial starts with `2, 3`, then adds random even gaps from `2` through `2 * max_gap_units`.",
            "A certified lock means the finite triangle reaches a certificate row before any first-column failure.",
            "These are finite seeded controls, not asymptotic probabilities.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["length", args.length],
                    ["trials_per_gap_unit", args.trials],
                    ["max_gap_units", args.max_gap_units],
                    ["seed_start", args.seed],
                ],
            ),
            "## Sweep Results",
            markdown_table(
                [
                    "max_gap_units",
                    "trials",
                    "certified_locks",
                    "certified_lock_rate",
                    "boundary_passes",
                    "certificates_found",
                    "avg_certificate_row",
                    "avg_failure_row",
                ],
                rows,
            ),
            "## Local Interpretation",
            "This sweep estimates how often random small-gap controls preserve the boundary `1` long enough to reach a certificate row.",
            "If prime prefixes behave like a high-rate random control, that constrains the Origin interpretation.",
            "If prime prefixes keep certified-lock behavior where matched controls rarely do, that gives a sharper candidate signal to investigate.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()

