"""Test Gilbreath behavior against k-history Markov prime-gap controls."""

from __future__ import annotations

import argparse
from datetime import datetime, timezone
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from emanation_from_1.conjectures import (  # noqa: E402
    certified_lock_scan,
    markov_gap_sequence_with_history,
)
from emanation_from_1.number_theory import first_n_primes  # noqa: E402


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    output = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        output.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(output)


def parse_csv_ints(value: str, label: str) -> list[int]:
    values = [int(part.strip()) for part in value.split(",") if part.strip()]
    if not values or any(item < 1 for item in values):
        raise ValueError(f"{label} must be a comma-separated list of positive integers")
    return values


def scan_control(
    primes: list[int],
    history: int,
    seed: int,
) -> tuple[bool, int | None, tuple[int, int] | None]:
    sequence = markov_gap_sequence_with_history(primes, history=history, seed=seed)
    scan = certified_lock_scan(sequence)
    return scan.certified, scan.certified_lock_row, scan.first_failure


def k_history_rows(args: argparse.Namespace) -> list[list[object]]:
    rows: list[list[object]] = []
    prime_counts = parse_csv_ints(args.prime_counts, "prime-counts")
    histories = parse_csv_ints(args.histories, "histories")

    for prime_count in prime_counts:
        primes = first_n_primes(prime_count)
        prime_scan = certified_lock_scan(primes)

        for history in histories:
            certified_count = 0
            lock_rows: list[int] = []
            failure_rows: list[int] = []

            for offset in range(args.trials):
                seed = args.seed + (prime_count * args.trials) + offset
                if history > 1:
                    seed += history * 1_000_000
                certified, lock_row, first_failure = scan_control(primes, history, seed)
                if certified:
                    certified_count += 1
                    lock_rows.append(lock_row or 0)
                elif first_failure is not None:
                    failure_rows.append(first_failure[0])

            average_lock = "none" if not lock_rows else f"{sum(lock_rows) / len(lock_rows):.2f}"
            average_failure = (
                "none" if not failure_rows else f"{sum(failure_rows) / len(failure_rows):.2f}"
            )

            rows.append(
                [
                    prime_count,
                    "none" if prime_scan.certified_lock_row is None else prime_scan.certified_lock_row,
                    history,
                    args.trials,
                    certified_count,
                    f"{certified_count / args.trials:.3f}",
                    average_lock,
                    average_failure,
                ]
            )

    return rows


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--prime-counts", default="256,512,1024,2048,4096")
    parser.add_argument("--histories", default="1,2,3")
    parser.add_argument("--trials", type=int, default=100)
    parser.add_argument("--seed", type=int, default=40000)
    parser.add_argument(
        "--output",
        type=Path,
        default=ROOT / "reports" / "GILBREATH_K_HISTORY_MARKOV_GAP_MODEL.md",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    histories = parse_csv_ints(args.histories, "histories")
    if args.trials < 1:
        raise ValueError("trials must be >= 1")
    if any(history < 1 for history in histories):
        raise ValueError("histories must be >= 1")

    generated = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    rows = k_history_rows(args)

    report = "\n\n".join(
        [
            "# Gilbreath K-History Markov Gap Model",
            f"Generated: `{generated}`",
            "This report is generated by `experiments/gilbreath_k_history_markov_gap_model.py`.",
            "Each control sequence is generated from empirical prime-gap transition maps with `k` prior gaps as state.",
            "For `k = 1`, this matches the earlier first-order Markov control and seed schedule. Larger `k` preserves longer local gap history at generation time.",
            "The generator preserves the first `k` real prime gaps and backs off to shorter histories only when a generated context has no exact empirical continuation.",
            "These are finite controls, not a proof of the infinite Gilbreath conjecture.",
            "## Parameters",
            markdown_table(
                ["parameter", "value"],
                [
                    ["prime_counts", args.prime_counts],
                    ["histories", args.histories],
                    ["trials_per_cell", args.trials],
                    ["seed_start", args.seed],
                ],
            ),
            "## K-History Markov Control Results",
            markdown_table(
                [
                    "prime_count",
                    "prime_lock_row",
                    "history",
                    "trials",
                    "certified_locks",
                    "lock_rate",
                    "avg_lock_row",
                    "avg_failure_row",
                ],
                rows,
            ),
            "## Local Interpretation",
            "If higher-history Markov controls remain below the real prime-prefix lock rate, short local gap dependency is still insufficient to explain the boundary-return behavior.",
            "If lock rates rise toward the real prime prefix as `k` increases, the Gilbreath signal is more likely explained by finite local gap-memory effects.",
            "The next stronger null after this would condition on wheel residues or preserve longer exact prime-gap blocks while still destroying global order.",
        ]
    )

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(report + "\n", encoding="utf-8")
    print(f"Wrote {args.output}")


if __name__ == "__main__":
    main()
